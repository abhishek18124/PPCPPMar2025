#include<iostream>

using namespace std;

int main() {

	char ch = 'A';
	cout << ch << endl;
	cout << (int)ch << endl;

	ch = 'd';
	cout << ch << endl;
	cout << (int)ch << endl;

	ch = '7';
	cout << ch << endl;
	cout << (int)ch << endl;

	ch = '8';
	cout << (int)ch << endl;
	cout << ch - '0' << endl;


	return 0;
}